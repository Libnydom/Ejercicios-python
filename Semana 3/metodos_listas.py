#| 3   | Métodos de listas | Dada una lista de números, encuentra el mayor, el menor y el promedio  |

lista_numeros=[1,2,3,4,5,6]
num_mayor=max(lista_numeros)
num_menor=min(lista_numeros)
num_prom= sum(lista_numeros) / len(lista_numeros)

print(lista_numeros)
print(num_mayor)
print(num_menor)
print(num_prom)

